# This program gets two numbers from user, sums them up and displays the sum value
# Get first number from User
num1 = float( input('Enter first number: ') )
print(num1)

# Get second number from User
num2 = float( input('Enter second number: ') )
print(num2)

# Calculate sum of the two numbers and print the sum
sum = num1 + num2
print('Sum value is ' + str(sum) )
